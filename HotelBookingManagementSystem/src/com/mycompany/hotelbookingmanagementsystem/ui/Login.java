    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        try{
        
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/hotel_booking_management_system", "root", "");

            String username=usernameTextField.getText();
            String password=passwordField.getText();

            Statement statement=con.createStatement();

            String sql="Select * from login where username='"+username+"'and Password='"+password+"'";
            ResultSet rs = statement.executeQuery(sql);

                if(rs.next()){
                    dispose();
                    MainDashboard hpage = new MainDashboard();
                    hpage.show();
                }else{
                    JOptionPane.showMessageDialog(this,"username and password is wrong");
                    usernameTextField.setText("");
                    passwordField.setText("");
                }
        }catch(HeadlessException | ClassNotFoundException | SQLException e){
            System.out.println(e.getMessage());
        }    
    }//GEN-LAST:event_loginButtonActionPerformed

